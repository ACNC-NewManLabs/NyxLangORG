/// Engine handle for loaded engines
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
pub struct EngineHandle(pub u64);

/// Engine capabilities
#[derive(Debug, Clone)]
pub struct Capabilities {
    /// Supported language features
    pub features: Vec<String>,
    /// Supported target platforms
    pub targets: Vec<String>,
    /// Maximum optimization level
    pub max_optimization_level: u8,
    /// Whether concurrent compilation is supported
    pub concurrent_compilation: bool,
    /// Additional capabilities
    pub extra: HashMap<String, String>,
}

impl Default for Capabilities {
    fn default() -> Self {
        Self {
            features: Vec::new(),
            targets: vec!["x86_64".to_string(), "aarch64".to_string()],
            max_optimization_level: 3,
            concurrent_compilation: true,
            extra: HashMap::new(),
        }
    }
}

/// Engine configuration
#[derive(Debug, Clone, Deserialize)]
pub struct EngineConfig {
    /// Engine name
    pub name: String,
    /// Engine version
    pub version: Version,
    /// Configuration parameters
    pub parameters: HashMap<String, String>,
    /// Enabled features
    pub features: Vec<String>,
}

impl EngineConfig {
    /// Create a new engine configuration
    pub fn new(name: impl Into<String>) -> Self {
        Self {
            name: name.into(),
            version: Version::default(),
            parameters: HashMap::new(),
            features: Vec::new(),
        }
    }

    /// Add a feature
    pub fn with_feature(mut self, feature: impl Into<String>) -> Self {
        self.features.push(feature.into());
        self
    }

    /// Add a parameter
    pub fn with_param(mut self, key: impl Into<String>, value: impl Into<String>) -> Self {
        self.parameters.insert(key.into(), value.into());
        self
    }
}

