/// Represents a compiled unit of code
#[derive(Debug, Clone)]
pub struct CompilationUnit {
    /// Unique identifier for the compilation unit
    pub id: String,
    /// Generated bytecode
    pub bytecode: Bytecode,
    /// Compilation warnings
    pub warnings: Vec<CompilationWarning>,
    /// Metadata about the compilation
    pub metadata: HashMap<String, String>,
}

impl CompilationUnit {
    /// Create a new compilation unit
    pub fn new(id: String, bytecode: Bytecode) -> Self {
        Self {
            id,
            bytecode,
            warnings: Vec::new(),
            metadata: HashMap::new(),
        }
    }
}

/// Compilation warning
#[derive(Debug, Clone)]
pub struct CompilationWarning {
    /// Warning code
    pub code: String,
    /// Warning message
    pub message: String,
    /// Source location
    pub location: SourceLocation,
}

/// Source location in code
#[derive(Debug, Clone)]
pub struct SourceLocation {
    /// File path
    pub file: PathBuf,
    /// Line number (1-indexed)
    pub line: u32,
    /// Column number (1-indexed)
    pub column: u32,
}

/// Bytecode representation
#[derive(Debug, Clone)]
pub struct Bytecode {
    /// Raw bytecode data
    pub data: Vec<u8>,
    /// Bytecode format version
    pub version: Version,
    /// Target architecture
    pub target: String,
}

/// Type check result
#[derive(Debug, Clone)]
pub struct TypeCheckResult {
    /// Whether type checking passed
    pub success: bool,
    /// Type errors found
    pub errors: Vec<TypeError>,
    /// Type warnings
    pub warnings: Vec<CompilationWarning>,
    /// Inferred type information
    pub type_info: HashMap<String, TypeInfo>,
}

/// Type error
#[derive(Debug, Clone)]
pub struct TypeError {
    /// Error code
    pub code: String,
    /// Error message
    pub message: String,
    /// Source location
    pub location: SourceLocation,
}

/// Type information
#[derive(Debug, Clone)]
pub struct TypeInfo {
    /// Type name
    pub name: String,
    /// Type kind
    pub kind: TypeKind,
}

/// Type kind
#[derive(Debug, Clone)]
pub enum TypeKind {
    /// Primitive type
    Primitive(PrimitiveType),
    /// Function type
    Function(Vec<TypeInfo>, Box<TypeInfo>),
    /// Array type
    Array(Box<TypeInfo>, Option<usize>),
    /// Struct type
    Struct(HashMap<String, TypeInfo>),
    /// Enum type
    Enum(Vec<(String, Vec<TypeInfo>)>),
    /// Unknown type
    Unknown,
}

/// Primitive types
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum PrimitiveType {
    /// 8-bit signed integer
    I8,
    /// 16-bit signed integer
    I16,
    /// 32-bit signed integer
    I32,
    /// 64-bit signed integer
    I64,
    /// 8-bit unsigned integer
    U8,
    /// 16-bit unsigned integer
    U16,
    /// 32-bit unsigned integer
    U32,
    /// 64-bit unsigned integer
    U64,
    /// 32-bit floating point
    F32,
    /// 64-bit floating point
    F64,
    /// Boolean
    Bool,
    /// Character
    Char,
    /// String
    String,
    /// Unit type (void)
    Unit,
}

