/// CompilerAPI - Public compiler interface
///
/// This trait provides the public API for compiling source code.
/// Implementations provide different compilation strategies and backends.
pub trait CompilerAPI {
    /// Compile source code into a compilation unit
    fn compile(&mut self, source: &str) -> Result<CompilationUnit, NyxError>;

    /// Type check source code without generating output
    fn check(&mut self, source: &str) -> Result<TypeCheckResult, NyxError>;

    /// Get the compiler version
    fn get_version(&self) -> Version;

    /// Get supported language features
    fn supported_features(&self) -> Vec<String>;

    /// Set compiler optimization level
    fn set_optimization_level(&mut self, level: u8) -> Result<(), NyxError>;

    /// Add a compilation pass
    fn add_pass(&mut self, pass: Box<dyn crate::extensions::CompilerPass>) -> Result<(), NyxError>;
}

/// RuntimeAPI - Public runtime interface
///
/// This trait provides the public API for executing compiled code.
/// Implementations provide different runtime environments and execution strategies.
pub trait RuntimeAPI {
    /// Execute bytecode and return a value
    fn execute(&self, bytecode: &Bytecode) -> Result<Value, NyxError>;

    /// Create an isolated sandbox for execution
    fn create_sandbox(&self, config: SandboxConfig) -> Result<Sandbox, NyxError>;

    /// Execute bytecode in a sandbox
    fn execute_in_sandbox(&self, bytecode: &Bytecode, sandbox: &Sandbox) -> Result<Value, NyxError>;

    /// Get runtime capabilities
    fn get_capabilities(&self) -> RuntimeCapabilities;

    /// Set up runtime environment
    fn initialize(&mut self, config: RuntimeConfig) -> Result<(), NyxError>;
}

/// Runtime capabilities
#[derive(Debug, Clone)]
pub struct RuntimeCapabilities {
    /// Supported bytecode versions
    pub supported_versions: Vec<Version>,
    /// Maximum concurrent executions
    pub max_concurrent: usize,
    /// Garbage collection support
    pub gc_support: bool,
    /// JIT compilation support
    pub jit_support: bool,
}

impl Default for RuntimeCapabilities {
    fn default() -> Self {
        Self {
            supported_versions: vec![Version::default()],
            max_concurrent: 4,
            gc_support: true,
            jit_support: false,
        }
    }
}

/// Runtime configuration
#[derive(Debug, Clone)]
pub struct RuntimeConfig {
    /// Enable garbage collection
    pub gc_enabled: bool,
    /// Enable JIT compilation
    pub jit_enabled: bool,
    /// Number of worker threads
    pub worker_threads: usize,
    /// Memory limit in bytes
    pub memory_limit: Option<usize>,
}

impl Default for RuntimeConfig {
    fn default() -> Self {
        Self {
            gc_enabled: true,
            jit_enabled: false,
            worker_threads: 4,
            memory_limit: Some(512 * 1024 * 1024), // 512 MB
        }
    }
}

/// EngineAPI - Engine loading interface
///
/// This trait provides the public API for loading and managing
/// language engines and execution backends.
pub trait EngineAPI {
    /// Load an engine by name
    fn load_engine(&mut self, name: &str) -> Result<EngineHandle, NyxError>;

    /// Unload an engine
    fn unload_engine(&mut self, handle: EngineHandle) -> Result<(), NyxError>;

    /// Get engine capabilities
    fn get_engine_capabilities(&self, handle: EngineHandle) -> Capabilities;

    /// Check if an engine is loaded
    fn is_engine_loaded(&self, name: &str) -> bool;

    /// List available engines
    fn list_engines(&self) -> Vec<String>;

    /// Get engine configuration
    fn get_engine_config(&self, handle: EngineHandle) -> Option<EngineConfig>;
}

/// ModuleAPI - Module loading interface
///
/// This trait provides the public API for loading, managing, and
/// resolving symbols in modules.
pub trait ModuleAPI {
    /// Load a module from the given path
    fn load_module(&self, path: &str) -> Result<ModuleId, NyxError>;

    /// Unload a module
    fn unload_module(&self, id: ModuleId) -> Result<(), NyxError>;

    /// Resolve a symbol in a module
    fn resolve_symbol(&self, module: ModuleId, symbol: &str) -> Result<Symbol, NyxError>;

    /// List all symbols in a module
    fn list_symbols(&self, module: ModuleId) -> Result<Vec<Symbol>, NyxError>;

    /// Get module dependencies
    fn get_dependencies(&self, module: ModuleId) -> Result<Vec<ModuleId>, NyxError>;

    /// Reload a module
    fn reload_module(&self, id: ModuleId) -> Result<ModuleId, NyxError>;
}

